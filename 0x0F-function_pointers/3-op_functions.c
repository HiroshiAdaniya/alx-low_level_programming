#include "3-calc.h"
/**
 * op_add - returns the sum of two ints
 * @a: int
 * @b: int
 * Return: an integer
 */
int op_add(int a, int b)
{

	return (a + b);
}
/**
 * op_sub - finds the difference between two ints
 * @a: int
 * @b: int
 * Return: an integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - determines the product of two ints
 * @a: int
 * @b: int
 * Return: an integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - gives the division of two ints
 * @a: int
 * @b: int
 * Return: an integer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - gives the remainder of a dived by b
 * @a: int
 * @b: int
 * Return: an integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
